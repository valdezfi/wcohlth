"use client";

import React, { useEffect, useState } from "react";
import { useModal } from "../../hooks/useModal";
import { Modal } from "../ui/modal";
import Button from "../ui/button/Button";
import Input from "../form/input/InputField";
import Label from "../form/Label";
import { useSession } from "next-auth/react";

export default function UserMetaCard() {
  const { data: session } = useSession();
  const user = session?.user;

  const { isOpen, openModal, closeModal } = useModal();
  const [fullName, setFullName] = useState<string>(""); // state uses camelCase
  const [profileImage, setProfileImage] = useState<string | null>(null);
  const [xUrl, setXUrl] = useState<string>("");
  const [about, setAbout] = useState<string>("");

  useEffect(() => {
    if (!user?.email) return;

    const fetchUserInfo = async () => {
      try {
        const [infoRes, imageRes] = await Promise.all([
          fetch(`http://localhost:5000/api/crypto/getgeneralinfo/${user.email}`),
          fetch(`http://localhost:5000/api/crypto/getprofileimage/${user.email}`),
        ]);

        if (!infoRes.ok || !imageRes.ok) {
          console.error("Failed to fetch user info or profile image");
          return;
        }

        const infoData = await infoRes.json();
        const imgData = await imageRes.json();

        // Map backend fullname to camelCase state
        setFullName(infoData.fullname || infoData.fullName || "");
        setAbout(infoData.about || "");
        setXUrl(infoData.tik || "");
        setProfileImage(imgData.imageUrl || null);
      } catch (err) {
        console.error("Failed to fetch user info:", err);
      }
    };

    fetchUserInfo();
  }, [user?.email]);

  const handleSave = async () => {
    if (!user?.email) return;

    try {
      // Trim and only include non-empty fields for update payload
      const requestBody: Record<string, string> = {};
      if (fullName.trim()) requestBody.fullname = fullName.trim(); // backend expects "fullname"
      if (xUrl.trim()) requestBody.tik = xUrl.trim();
      if (about.trim()) requestBody.about = about.trim();

      if (Object.keys(requestBody).length === 0) {
        console.warn("No valid data to update");
        return;
      }

      console.log("Sending update:", requestBody);

      const res = await fetch(`http://localhost:5000/api/crypto/updategeneralinfo/${user.email}`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(requestBody),
      });

      if (!res.ok) {
        const errText = await res.text();
        throw new Error(`Failed to update info: ${res.status} ${errText}`);
      }

      closeModal();
    } catch (err) {
      console.error("Save error:", err);
    }
  };

  return (
    <>
      <div className="p-5 border border-gray-200 rounded-2xl dark:border-gray-800 lg:p-6">
        <div className="flex flex-col gap-5 xl:flex-row xl:items-center xl:justify-between">
          <div className="flex flex-col items-center w-full gap-6 xl:flex-row">
            <div className="w-20 h-20 overflow-hidden border border-gray-200 rounded-full dark:border-gray-800">
              <img
                src={profileImage || "/images/user/owner.jpg"}
                alt={fullName || "User"}
                width={100}
                height={100}
                className="w-full h-full object-cover rounded-full"
              />
            </div>
            <div className="order-3 xl:order-2">
              <h4 className="mb-2 text-lg font-semibold text-center text-gray-800 dark:text-white/90 xl:text-left">
                {fullName || "Unnamed User"}
              </h4>
              <div className="flex flex-col items-center gap-1 text-center xl:flex-row xl:gap-3 xl:text-left">
                <p className="text-sm text-gray-500 dark:text-gray-400">
                  {about || "No bio provided."}
                </p>
              </div>
            </div>
            <div className="flex items-center order-2 gap-2 grow xl:order-3 xl:justify-end">
              {xUrl && (
                <a
                  href={xUrl}
                  target="_blank"
                  rel="noreferrer"
                  className="flex h-11 w-11 items-center justify-center rounded-full border border-gray-300 bg-white text-gray-700 shadow-theme-xs hover:bg-gray-50 dark:border-gray-700 dark:bg-gray-800 dark:text-gray-400 dark:hover:bg-white/[0.03] dark:hover:text-gray-200"
                >
                  <svg className="fill-current" width="20" height="20" viewBox="0 0 20 20">
                    <path d="M15.1708 1.875H17.9274L11.9049 8.75833L18.9899 18.125H13.4424L9.09742 12.4442L4.12578 18.125H1.36745L7.80912 10.7625L1.01245 1.875H6.70078L10.6283 7.0675L15.1708 1.875ZM14.2033 16.475H15.7308L5.87078 3.43833H4.23162L14.2033 16.475Z" />
                  </svg>
                </a>
              )}
            </div>
          </div>
          <button
            onClick={openModal}
            className="flex w-full items-center justify-center gap-2 rounded-full border border-gray-300 bg-white px-4 py-3 text-sm font-medium text-gray-700 shadow-theme-xs hover:bg-gray-50 hover:text-gray-800 dark:border-gray-700 dark:bg-gray-800 dark:text-gray-400 dark:hover:bg-white/[0.03] dark:hover:text-gray-200 lg:inline-flex lg:w-auto"
          >
            Edit
          </button>
        </div>
      </div>

      <Modal isOpen={isOpen} onClose={closeModal} className="max-w-[700px] m-4">
        <div className="no-scrollbar w-full max-w-[700px] overflow-y-auto rounded-3xl bg-white p-4 dark:bg-gray-900 lg:p-11">
          <div className="px-2 pr-14">
            <h4 className="mb-2 text-2xl font-semibold text-gray-800 dark:text-white/90">
              Edit Personal Information
            </h4>
            <p className="mb-6 text-sm text-gray-500 dark:text-gray-400">
              Update your details to keep your profile up-to-date.
            </p>
          </div>
          <form
            className="flex flex-col"
            onSubmit={(e) => {
              e.preventDefault();
              handleSave();
            }}
          >
            <div className="custom-scrollbar h-[450px] overflow-y-auto px-2 pb-3">
              <div>
                <h5 className="mb-5 text-lg font-medium text-gray-800 dark:text-white/90">
                  Social Links
                </h5>
                <div className="grid grid-cols-1 gap-x-6 gap-y-5 lg:grid-cols-2">
                  <div>
                    <Label>X.com</Label>
                    <Input
                      type="text"
                      value={xUrl}
                      onChange={(e) => setXUrl(e.target.value.trimStart())} // optional trim start
                    />
                  </div>
                </div>
              </div>
              <div className="mt-7">
                <h5 className="mb-5 text-lg font-medium text-gray-800 dark:text-white/90">
                  Personal Information
                </h5>
                <div className="grid grid-cols-1 gap-x-6 gap-y-5 lg:grid-cols-2">
                  <div className="col-span-2 lg:col-span-1">
                    <Label>Username</Label>
                    <Input
                      type="text"
                      value={fullName}
                      onChange={(e) => setFullName(e.target.value)}
                    />
                  </div>
                  <div className="col-span-2">
                    <Label>Bio</Label>
                    <Input
                      type="text"
                      value={about}
                      onChange={(e) => setAbout(e.target.value)}
                    />
                  </div>
                </div>
              </div>
            </div>
            <div className="flex items-center gap-3 px-2 mt-6 lg:justify-end">
              <Button size="sm" type="submit" variant="primary">
                Save Changes
              </Button>
            </div>
          </form>
        </div>
      </Modal>
    </>
  );
}
